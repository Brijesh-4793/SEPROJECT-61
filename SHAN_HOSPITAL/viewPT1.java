     public void appshow()
     {
        try {
            pst = con.prepareStatement("select * from prescription where DName = ?");
            pst.setString(1,Nname1);
            rs = pst.executeQuery();
            ResultSetMetaData Rsm = rs.getMetaData();
            int c;
            c = Rsm.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                for(int i=1;i<=c;i++){
                v2.add(rs.getString("PT_id"));
                v2.add(rs.getString("ApNo"));
                v2.add(rs.getString("DName"));
                v2.add(rs.getString("PName"));
                v2.add(rs.getString("DType"));
                v2.add(rs.getString("Des"));
                
             }
                df.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Appointment.class.getName()).log(Level.SEVERE, null, ex);
        }
     
     }

    
